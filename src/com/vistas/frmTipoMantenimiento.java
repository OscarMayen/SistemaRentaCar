    public void tablaTipoMantenimiento()
    {
        String [] columnas = {"id","Tipo"};
        Object [] obj=new Object[2];
        DefaultTableModel tabla = new DefaultTableModel(null,columnas);
        List ls;
       try
        {
            ls = daotM.MostrarTipoMantenimiento();
            for (int i=0; i<ls.size(); i++)
            {
                tipoM=(TipoMantenimiento)ls.get(i);
                obj[0]=tipoM.getIdTipoMantenimiento();
                obj[1]=tipoM.getTipoManteniento();
                
                tabla.addRow(obj);
            }
            ls=daotM.MostrarTipoMantenimiento();
            this.JTbTipoMantenimiento.setModel(tabla);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, "Error al mostrar datos" + e.toString());
        }
    }
    
    public void insertarTipoMantenimiento ()throws Exception
    {
        tipoM.setIdTipoMantenimiento(Integer.parseInt(this.txtIdTipoMantenimiento.getText()));
        
        tipoM.setTipoManteniento(this.txtTipoMantenimiento.getText());
        daotM.insertarTipoMantenimiento(tipoM);
        JOptionPane.showMessageDialog(null, "Datos insertados con exito");
        limpiar();
   }
    
    
     public void limpiar()
     {
        this.txtIdTipoMantenimiento.setText("");
        this.txtTipoMantenimiento.setText("");
     }
     
    public void llenarTabla()
    {
        int fila = this.JTbTipoMantenimiento.getSelectedRow();
        this.txtIdTipoMantenimiento.setText(String.valueOf(this.JTbTipoMantenimiento.getValueAt(fila,0)));
        this.txtTipoMantenimiento.setText(String.valueOf(this.JTbTipoMantenimiento.getValueAt(fila,1)));
    }
     
     public void modificar ()
     {
         try 
         {
            tipoM.setIdTipoMantenimiento(Integer.parseInt(this.txtIdTipoMantenimiento.getText()));
            tipoM.setTipoManteniento(this.txtTipoMantenimiento.getText());
            int SiONo=JOptionPane.showConfirmDialog(this, "Desea modificar el Tipo de Mantenimiento","Modificar Tipo de Mantenimiento",JOptionPane.YES_NO_OPTION);
            
             if (SiONo==0)
            {
                daotM.modificarTipoMantenimiento(tipoM);
                JOptionPane.showMessageDialog(rootPane, "Tipo de Mantenimiento modificado con exito","Confirmacion",JOptionPane.INFORMATION_MESSAGE);
                tablaTipoMantenimiento();
                limpiar();
            }
             
             else
             {
                 limpiar();
             }
         } 
        
         catch (Exception ex) 
         {
           ex.printStackTrace();
         }
     
     }
     
     public void eliminar()
     {
          try 
         {
            tipoM.setIdTipoMantenimiento(Integer.parseInt(this.txtIdTipoMantenimiento.getText()));
            int SiONo=JOptionPane.showConfirmDialog(this, "Desea eliminar el Tipo Persona?","Eliminar Tipo Persona",JOptionPane.YES_NO_OPTION);
            
             if (SiONo==0)
            {
                daotM.eliminarTipoMantenimiento(tipoM);
                JOptionPane.showMessageDialog(rootPane, "Tipo Mantenimiento eliminado con exito","Confirmacion",JOptionPane.INFORMATION_MESSAGE);
                tablaTipoMantenimiento();
                limpiar();
            }
             
             else
             {
                 limpiar();
             }
         } 
        
         
         catch (Exception ex) 
         {
           JOptionPane.showMessageDialog(rootPane, ex.toString(),"Error",JOptionPane.ERROR_MESSAGE);
                   
         }
     }


