    public void tablaDepartamentos() {
        String [] columnas = {"C贸digo","Nombe Departamento"};
        Object [] obj = new Object[2];
        DefaultTableModel tabla = new DefaultTableModel(null,columnas);
        List ls;
        try {
            ls = daoD.mostrarDepartamento();
            for (int i=0; i<ls.size();i++)
            {
                dpt=(Departamento)ls.get(i);
                obj[0]=dpt.getIdDepartamento();
                obj[1]=dpt.getNombre();
                tabla.addRow(obj);
                
            }
            ls=daoD.mostrarDepartamento();
            this.jTblDepartamentos.setModel(tabla);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Error al mostrar datos"+e.toString());
            
            
        }
    }
        
        public void insertarDepartamento ()throws Exception
    {
        dpt.setIdDepartamento(Integer.parseInt(this.jTxtCodigo.getText()));
        
        dpt.setNombre(this.jTxtNombre.getText());
        daoD.insertarDepartamento(dpt);
        JOptionPane.showMessageDialog(null, "Datos insertados con exito");
        limpiar();
   }
    
    
     public void limpiar()
     {
        this.jTxtCodigo.setText("");
        this.jTxtNombre.setText("");
     }
     
    public void llenarTabla()
    {
        int fila = this.jTblDepartamentos.getSelectedRow();
        this.jTxtCodigo.setText(String.valueOf(this.jTblDepartamentos.getValueAt(fila,0)));
        this.jTxtNombre.setText(String.valueOf(this.jTblDepartamentos.getValueAt(fila,1)));
    }
     
     public void modificar ()
     {
         try 
         {
            dpt.setIdDepartamento(Integer.parseInt(this.jTxtCodigo.getText()));
            dpt.setNombre(this.jTxtNombre.getText());
            int SiONo=JOptionPane.showConfirmDialog(this, "Desea modificar el Departamento","Modificar Departamento",JOptionPane.YES_NO_OPTION);
            
             if (SiONo==0)
            {
                daoD.modificarDepartamento(dpt);
                JOptionPane.showMessageDialog(rootPane, "Departamento modificado con exito","Confirmacion",JOptionPane.INFORMATION_MESSAGE);
                tablaDepartamentos();
                limpiar();
            }
             
             else
             {
                 limpiar();
             }
         } 
        
         catch (Exception ex) 
         {
           ex.printStackTrace();
         }
     
     }
     
     public void eliminar()
     {
          try 
         {
            dpt.setIdDepartamento(Integer.parseInt(this.jTxtCodigo.getText()));
            int SiONo=JOptionPane.showConfirmDialog(this, "Desea eliminar el Departamento?","Eliminar Departamento",JOptionPane.YES_NO_OPTION);
            
             if (SiONo==0)
            {
                daoD.eliminarDepartamento(dpt);
                JOptionPane.showMessageDialog(rootPane, "Departamento eliminado con exito","Confirmacion",JOptionPane.INFORMATION_MESSAGE);
                tablaDepartamentos();
                limpiar();
            }
             
             else
             {
                 limpiar();
             }
         } 
        
         
         catch (Exception ex) 
         {
           JOptionPane.showMessageDialog(rootPane, ex.toString(),"Error",JOptionPane.ERROR_MESSAGE);
                   
         }
     
        
        
    }
